public class TipeDataString {
    public static void main(String[] args) {
        String kata = "Aku";
        String kalimat = "Belajar Java";

        String gabunganString = kata + kalimat;
        System.out.println(kata + kalimat);
        System.out.println(gabunganString);
        System.out.println(kata + " Belajar Java");
    }
}
